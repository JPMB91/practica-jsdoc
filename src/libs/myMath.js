/**
 * Adds two numbers
 * @param {Number} x First number
 * @param {Number} y Second number
 * @returns {Number} Sum of x and y
 */
const addNumbers = (x, y) => x + y;

/**
 * Substract two numbers
 * @param {Number} x First number
 * @param {Number} y Second number
 * @returns {Number} substract of two numbers
 */
const substract = (x, y) => x - y;

module.exports = {
  addNumbers,
  substract,
};
